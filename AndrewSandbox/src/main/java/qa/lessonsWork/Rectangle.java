package qa.lessonsWork;

public class Rectangle {

  public double lung;
  public double lats;

  public Rectangle(double lung,double lats){

    this.lung=lung;
    this.lats=lats;
    //constructor
  }

  public double ariadreptunghi() {

    return this.lung * this.lats;
    //metoda "ariadreptunghi" care e asociata cu obiectul Rectangle
  }

}
